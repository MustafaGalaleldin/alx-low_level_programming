#include "3-calc.h"

/**
 * op_add - returns the sum
 * @a: int
 * @b: int
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the sub
 * @a: int
 * @b: int
 * Return: sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the mul
 * @a: int
 * @b: int
 * Return: mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the div
 * @a: int
 * @b: int
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder
 * @a: int
 * @b: int
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
